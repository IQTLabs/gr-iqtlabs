class pdu_decoder(gr.sync_block):
    def __init__(self):
        gr.sync_block.__init__(
            self,
            name="pdu_decoder",
            in_sig=[],
            out_sig=[],
        )
        self.message_port_register_in(pmt.intern("pdu"))
        self.set_msg_handler(pmt.intern("pdu"), self.receive_pdu)
        self.decompressor = zstandard.ZstdDecompressor()
        self.json_output = []
        self.compressed_received = 0
        self.decompressed_received = 0

    def receive_pdu(self, pdu):
        compressed_payload = bytes(pmt.to_python(pmt.cdr(pdu)))
        self.compressed_received += len(compressed_payload)
        with self.decompressor.stream_reader(compressed_payload) as reader:
            decompressed_payload = reader.read().decode("utf-8")
            self.decompressed_received += len(decompressed_payload)
            self.json_output.append(json.loads(decompressed_payload))


class qa_retune_fft_base:
    def __init__(self):
        self.tb = None

    def retune_fft(self, fft_roll, stare):
        points = int(2048)
        samp_rate = int(points * points)
        tune_step_hz = samp_rate
        tune_step_fft = int(256)
        skip_tune_step_fft = int(16)
        freq_start = int(1e9 / samp_rate) * samp_rate
        if stare:
            freq_end = freq_start + (tune_step_hz / 2)
            tuning_ranges = f"{freq_start}-{freq_end}"
        else:
            freq_end = int(1.1e9 / samp_rate) * samp_rate
            freq_mid = ((freq_end - freq_start) / 2) + freq_start
            tuning_ranges = f"{freq_start}-{freq_mid},{freq_mid+samp_rate}-{freq_end}"

        fft_write_count = 2
        bucket_range = 1
        fft_min = -1e9
        fft_batch_size = 1

        with tempfile.TemporaryDirectory() as tmpdir:
            test_file = os.path.join(tmpdir, "samples.csv")
            iqtlabs_tuneable_test_source_0 = tuneable_test_source(0, freq_end)
            iqtlabs_retune_pre_fft_0 = retune_pre_fft(
                nfft=points,
                samp_rate=samp_rate,
                tune_jitter_hz=0,
                fft_batch_size=1,
                tag="rx_freq",
                freq_start=int(freq_start),
                freq_end=int(freq_end),
                tune_step_hz=tune_step_hz,
                tune_step_fft=tune_step_fft,
                skip_tune_step_fft=skip_tune_step_fft,
                tuning_ranges=tuning_ranges,
                tag_now=False,
                low_power_hold_down=False,
                slew_rx_time=False,
            )
            iqtlabs_retune_fft_0 = retune_fft(
                tag="rx_freq",
                nfft=points,
                samp_rate=samp_rate,
                tune_jitter_hz=0,
                freq_start=int(freq_start),
                freq_end=int(freq_end),
                tune_step_hz=tune_step_hz,
                tune_step_fft=tune_step_fft,
                skip_tune_step_fft=skip_tune_step_fft,
                fft_min=fft_min,
                fft_max=1e9,
                sdir=tmpdir,
                write_step_fft=fft_write_count,
                bucket_range=bucket_range,
                tuning_ranges=tuning_ranges,
                description="a text description",
                rotate_secs=3600,
                pre_fft=True,
                tag_now=False,
                low_power_hold_down=False,
                slew_rx_time=False,
                peak_fft_range=0,
            )
            pdu_decoder_0 = pdu_decoder()
            fft_vxx_0 = fft.fft_vcc(points, True, [], fft_roll, 1)

            try:
                if os.getenv("TEST_VKFFT", 0):
                    from gnuradio.iqtlabs import vkfft

                    fft_vxx_0 = vkfft(fft_batch_size * points, points, fft_roll)
                    print("using VkFFT")
            except ImportError:
                print("using software FFT")
            window = blocks.multiply_const_vff(
                [val for val in fft.window.blackmanharris(points) for _ in range(2)]
                * fft_batch_size
            )
            blocks_throttle_0 = blocks.throttle(
                gr.sizeof_gr_complex * 1, samp_rate, True
            )
            blocks_file_sink_0 = blocks.file_sink(gr.sizeof_char * 1, test_file, False)
            blocks_file_sink_0.set_unbuffered(False)
            blocks_null_sink_0 = blocks.null_sink(gr.sizeof_float * points)

            blocks_complex_to_mag_0 = blocks.complex_to_mag(points)
            blocks_nlog10_ff_0 = blocks.nlog10_ff(20, points, 0)
            vr1 = vector_roll(points)

            self.tb.msg_connect(
                (iqtlabs_retune_pre_fft_0, "tune"),
                (iqtlabs_tuneable_test_source_0, "cmd"),
            )
            self.tb.msg_connect(
                (iqtlabs_retune_pre_fft_0, "tune"), (iqtlabs_retune_fft_0, "cmd")
            )
            self.tb.msg_connect((iqtlabs_retune_fft_0, "json"), (pdu_decoder_0, "pdu"))
            self.tb.connect((blocks_complex_to_mag_0, 0), (blocks_nlog10_ff_0, 0))
            self.tb.connect((blocks_nlog10_ff_0, 0), (iqtlabs_retune_fft_0, 0))
            if fft_roll:
                self.tb.connect((fft_vxx_0, 0), (blocks_complex_to_mag_0, 0))
            else:
                # double roll, is a no-op
                self.tb.connect((fft_vxx_0, 0), (vr1, 0))
                self.tb.connect((vr1, 0), (blocks_complex_to_mag_0, 0))
            self.tb.connect((iqtlabs_retune_fft_0, 0), (blocks_file_sink_0, 0))
            self.tb.connect((iqtlabs_retune_fft_0, 1), (blocks_null_sink_0, 0))
            self.tb.connect((iqtlabs_retune_pre_fft_0, 0), (window, 0))
            self.tb.connect((window, 0), (fft_vxx_0, 0))
            self.tb.connect((blocks_throttle_0, 0), (iqtlabs_retune_pre_fft_0, 0))
            self.tb.connect((iqtlabs_tuneable_test_source_0, 0), (blocks_throttle_0, 0))

            self.tb.start()

            # Since test source generates the same samples for the same frequency value,
            # the same frequency must have the same power for repeated observations.
            records = []
            bucket_counts = defaultdict(int)
            tuning_range_changes = 0

            startup_timeout = 1
            for _ in range(10):
                if os.path.exists(test_file):
                    break
                time.sleep(startup_timeout)
            self.assertTrue(os.path.exists(test_file))

            with open(test_file, encoding="utf8") as f:
                try:
                    linebuffer = ""
                    last_data = time.time()
                    last_ts = 0
                    last_buckets = None
                    last_tuning_range = None
                    file_poll_timeout = 0.001
                    while (stare and len(records) < 1000) or (
                        not stare and tuning_range_changes < 5
                    ):
                        self.assertLess(time.time() - last_data, 5)
                        line = f.readline()
                        linebuffer = linebuffer + line
                        if not linebuffer.endswith("\n"):
                            time.sleep(file_poll_timeout)
                            continue
                        last_data = time.time()
                        line = linebuffer.strip()
                        linebuffer = ""
                        record = json.loads(line)
                        ts = round(record["ts"])
                        self.assertGreaterEqual(ts, last_ts)
                        last_ts = ts
                        config = record["config"]
                        self.assertEqual("a text description", config["description"]),
                        tuning_range_freq_start = config["tuning_range_freq_start"]
                        tuning_range_freq_end = config["tuning_range_freq_end"]
                        tuning_range = int(config["tuning_range"])
                        if tuning_range != last_tuning_range:
                            tuning_range_changes += 1
                            print("tuning_range_changes:", tuning_range_changes)
                        last_tuning_range = tuning_range
                        if not stare:
                            self.assertTrue(
                                (
                                    tuning_range_freq_start == freq_start
                                    and tuning_range_freq_end == freq_mid
                                )
                                or (
                                    tuning_range_freq_start == freq_mid + samp_rate
                                    and tuning_range_freq_end == freq_end
                                )
                            )
                        self.assertEqual(config["freq_start"], freq_start)
                        self.assertEqual(config["freq_end"], freq_end)
                        self.assertEqual(config["sample_rate"], samp_rate)
                        self.assertEqual(config["nfft"], points)
                        buckets = record["buckets"]
                        self.assertTrue(buckets, (last_buckets, buckets))
                        bucket_counts[len(buckets)] += 1
                        fs = [int(f) for f in buckets.keys()]
                        self.assertGreaterEqual(min(fs), tuning_range_freq_start)
                        self.assertLessEqual(max(fs), tuning_range_freq_end)
                        new_records = [
                            {
                                "ts": ts,
                                "f": int(freq),
                                "v": float(value),
                                "t": int(tuning_range),
                            }
                            for freq, value in buckets.items()
                        ]
                        records.extend(new_records)
                        last_buckets = buckets
                except Exception:
                    self.tb.stop()
                    raise

            self.tb.stop()
            self.tb.wait()
            os.remove(test_file)

            top_count = sorted(bucket_counts.items(), key=lambda x: x[1], reverse=True)[
                0
            ]
            if not stare:
                expected_buckets = round(points * bucket_range)
                self.assertEqual(top_count[0], expected_buckets)
            all_df = pd.DataFrame(records)[["t", "f", "v"]]
            all_df["v"] = all_df["v"].round(1)
            all_df = all_df.sort_values(["t", "f", "v"])
            self.assertEqual(all_df["f"].max(), freq_end)
            self.assertEqual(all_df["f"].min(), freq_start)

            for _, df in all_df.groupby("t"):
                # must have plausible unscaled dB value
                self.assertTrue(fft_min <= df["v"].min() <= 1, (fft_min, df["v"].min()))
                self.assertTrue(50 <= df["v"].max() <= 61, df["v"].max())
                df["m"] = df.groupby("f")["v"].apply(lambda x: x.max() - x.min())
                non_unique_v = df[df["m"] > 2]
                # every frequency must have power values within 1-2dB (accounts for precision loss in FFT block)
                self.assertTrue(non_unique_v.empty, non_unique_v)
                # pd.set_option("display.max_rows", None)
                # every frequency must be observed more than once.
                df["u"] = df.groupby("f")["f"].transform("count")
                if stare:
                    self.assertEqual(df["u"].min(), 1, df[df["u"] != 1])
                else:
                    self.assertGreater(df["u"].min(), 1, df[df["u"] == 1])
                # must have even frequency coverage within the range
                df["d"] = df["f"].diff()
                df = df[(df["d"] != 0) & (df["d"].notna())]
                self.assertTrue(df[df["d"] != points].empty, df[df["d"] != points])

            hz_re = re.compile(".+_([0-9]+)Hz.+")
            first_zst_fft_file = sorted(glob.glob(os.path.join(tmpdir, "*/*.zst")))[:1][
                0
            ]
            first_hz_match = hz_re.match(first_zst_fft_file)
            if not first_hz_match:
                self.fail(f"{first_zst_fft_file} did not match regexp")
                return
            first_hz = int(first_hz_match.group(1))
            zst_fft_files = sorted(
                glob.glob(
                    os.path.join(tmpdir, f"*/*{first_hz}Hz_{samp_rate}sps.raw.zst")
                )
            )
            if stare:
                self.assertGreater(len(zst_fft_files), 1)
            else:
                self.assertGreater(len(zst_fft_files), 2)
            first_sample = None
            for zst_file in zst_fft_files:
                subprocess.check_call(["zstd", "-d", zst_file])
                bin_file = zst_file.replace(".zst", "")
                sample = np.fromfile(bin_file, dtype=np.float32)
                if len(sample):
                    if first_sample is None:
                        first_sample = sample
                    self.assertTrue(np.array_equal(first_sample, sample))
                    self.assertGreater(len(np.unique(sample)), 1)
                    self.assertEqual(len(sample), fft_write_count * points)
                os.remove(zst_file)

            remaining_files = glob.glob(os.path.join(tmpdir, "*/*"))
            print(f"remaining {remaining_files}")

            self.assertTrue(pdu_decoder_0.json_output)
            self.assertGreater(
                pdu_decoder_0.decompressed_received, pdu_decoder_0.compressed_received
            )


